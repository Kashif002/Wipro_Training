package com.wipro;

public class first_program{
	//Entry point
	public static void main(String[] args) {
		System.out.println("Hello Java Learners");
		
		//Implicit TypeCasting
		byte a=10;
		short b =a;
		System.out.println(b);
		
		//Explicit TypeCasting
		short e=25;
		byte f= (byte)e;
		System.out.println(f);
		
	}
}